	"Maximum_Attendees"	INTEGER NOT NULL,
	PRIMARY KEY("Id")
);
CREATE TABLE IF NOT EXISTS "Attendees" (
	"Id"	TEXT NOT NULL,
	"Name"	TEXT NOT NULL,
	"Email"	TEXT NOT NULL,
	"Event_Id"	TEXT NOT NULL,
	"Created_At"	TEXT NOT NULL,
	PRIMARY KEY("Id"),
	CONSTRAINT "attendees_event_id_fkey" FOREIGN KEY("Event_Id") REFERENCES "Events"("id")
);
CREATE TABLE IF NOT EXISTS "CheckIns" (
	"Id"	TEXT NOT NULL,
